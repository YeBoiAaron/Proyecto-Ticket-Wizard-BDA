    /**M√©todo para crear un componente compuesto con nombre del evento, descripci√≥n, imagen relacionada al evento y bot√≥n para 
     * 
     * @param nombre Nombre del evento
     * @param descripcion Breve descripci√≥n del evento
     * @param imagePath Icono del evento
     * @return Panel que encapsula los elementos que representan a un evento
     */
    public JPanel crearComponenteCompuesto(String nombre, String descripcion, String imagePath) {
        // Crear panel para el componente compuesto
        JPanel panel = new JPanel();
        panel.setLayout(new BorderLayout());
        panel.setBorder(BorderFactory.createEmptyBorder(10, 10, 10, 10)); // A√±adir m√°rgenes
        
        // Crear panel para los elementos de la derecha (nombre, descripci√≥n y bot√≥n)
        JPanel rightPanel = new JPanel();
        rightPanel.setLayout(new BoxLayout(rightPanel, BoxLayout.Y_AXIS));
        
        // Crear etiqueta para el nombre
        JLabel nameLabel = new JLabel(nombre);
        nameLabel.setFont(new Font("Arial", Font.BOLD, 14));
        
        // Crear etiqueta para la descripci√≥n
        JLabel descriptionLabel = new JLabel("<html><p style='width:200px;'>" + descripcion + "</p></html>"); // Para que el texto se ajuste en varias l√≠neas
        
        // Crear bot√≥n
        JButton button = new JButton("Ver boletos en reventa");
        
        // A√±adir un ActionListener a cada bot√≥n
        button.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                // Acci√≥n que se ejecuta cuando el bot√≥n es presionado
                //TODO a√±adir una ventana con los boletos del evento
            }
        });
        
        // A√±adir nombre, descripci√≥n y bot√≥n al panel derecho
        rightPanel.add(nameLabel);
        rightPanel.add(descriptionLabel);
        rightPanel.add(button);
        
        // Cargar imagen (por simplicidad, usa un icono en blanco si la imagen no est√° disponible)
        ImageIcon imageIcon = new ImageIcon(imagePath); // Usa la ruta de la imagen adecuada
        JLabel imageLabel = new JLabel();
        if (imageIcon.getIconWidth() > 0) {
            imageLabel.setIcon(imageIcon);
        } else {
            // Si la imagen no est√° disponible, usar un espacio en blanco o una imagen por defecto
            imageLabel.setIcon(new ImageIcon(new BufferedImage(100, 100, BufferedImage.TYPE_INT_ARGB)));
        }
        
        // A√±adir los paneles izquierdo y derecho (imagen) al componente principal
        panel.add(rightPanel, BorderLayout.CENTER); // Panel con nombre, descripci√≥n y bot√≥n a la derecha
        panel.add(imageLabel, BorderLayout.WEST);  // Imagen a la izquierda
        
        return panel;
    }
    
    /**
     * Encapsula cada uno de los paneles generados por evento en un solo JPanel
     * @return JPanel con todos los eventos encapsulados
     */
    public JPanel encapsularEventos() {
        JPanel panelPrincipal = new JPanel();
        panelPrincipal.setLayout(new BoxLayout(panelPrincipal, BoxLayout.Y_AXIS));
        for (int i = 0; i < controlEventos.cantidadTotalEventos(); i++) {
            panelPrincipal.add(crearComponenteCompuesto(eventoBO.consultarEventos().get(i).getNombre(), controlEventos.descripcionEvento(i), "/main/java/imgs/evento1.png"));
        }
        return panelPrincipal;
    }

